class Solution:
    def superPow(self, a: int, b: List[int]) -> int:

        c = 0
        for d in b:
            c = (c * 10 + d) % 1140

        a = a % 1337

        if c % 2 == 1:
            result = a
        else:
            result = 1
        while c:
            c = c // 2
            a = (a * a) % 1337
            if c % 2 == 1:
                result = (result * a) % 1337
        return result


